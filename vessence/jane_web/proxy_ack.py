def pick_ack(user_message: str, *, chooser: Callable[[Sequence[str]], str] | None = None) -> str | None:
    """Pick a context-appropriate quick ack based on the user's message."""

    msg = (user_message or "").lower().strip()

    for rule in _ACK_RULES:
        if _matches_rule(msg, rule):
            return _choose(rule.options, chooser)

    return None
