"""Quick acknowledgement selection for Jane proxy."""

from __future__ import annotations

import random
from collections.abc import Callable, Sequence
from dataclasses import dataclass


@dataclass(frozen=True)
class _AckRule:
    patterns: tuple[str, ...]
    options: tuple[str, ...]
    startswith: bool = False


def _choose(options: Sequence[str], chooser: Callable[[Sequence[str]], str] | None) -> str:
    return (chooser or random.choice)(options)


def _matches_rule(msg: str, rule: _AckRule) -> bool:
    if rule.startswith:
        return msg.startswith(rule.patterns)
    return any(pattern in msg for pattern in rule.patterns)


